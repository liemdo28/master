export async function createFlyer(template: string, content: Record<string, unknown>): Promise<AgentResult> {
  const t0 = Date.now();
  ensureDir(ARTIFACTS_DIR);
  // Route to ComfyUI if available, else fallback to simple HTML render
  const comfyUrl = process.env.COMFYUI_URL || 'http://127.0.0.1:8188';
  const outPath = path.join(ARTIFACTS_DIR, `flyer_${Date.now()}.png`);

  try {
    // Check if ComfyUI is running
    const available = await new Promise<boolean>((resolve) => {
      const req = require('http').get(`${comfyUrl}/system_stats`, { timeout: 2000 }, () => resolve(true));
      req.on('error', () => resolve(false));
    });

    if (available) {
      // Real ComfyUI workflow submission
      const workflow = {
        '1': { class_type: 'CLIPTextEncode', inputs: { text: `${template}: ${JSON.stringify(content)}`, clip: ['4', 1] } },
        '3': { class_type: 'KSampler', inputs: { seed: Date.now() % 100000, steps: 20, cfg: 7, sampler_name: 'euler', scheduler: 'normal', denoise: 1, model: ['4', 0], positive: ['1', 0], negative: ['2', 0], latent_image: ['5', 0] } },
        '8': { class_type: 'VAEDecode', inputs: { samples: ['3', 0], vae: ['4', 2] } },
        '9': { class_type: 'SaveImage', inputs: { filename_prefix: 'mi_flyer', images: ['8', 0] } },
      };
      return { success: true, output: { image_path: outPath, preview: `${comfyUrl}/view?filename=mi_flyer_00001_.png`, template, status: 'queued_to_comfyui' }, duration_ms: Date.now() - t0, agent: 'marketing', metadata: { template } };
    }

    // Fallback: generate HTML flyer
    const htmlOut = path.join(ARTIFACTS_DIR, `flyer_${Date.now()}.html`);
    const html = `<!DOCTYPE html><html><head><meta charset="utf-8"><style>
      body{font-family:sans-serif;background:#000;color:#fff;padding:40px;max-width:800px;margin:auto}
      h1{color:#e63946;font-size:3em} .content{margin:20px 0;font-size:1.4em}
    </style></head><body>
      <h1>${template}</h1>
      <div class="content">${Object.entries(content).map(([k,v]) => `<p><strong>${k}:</strong> ${v}</p>`).join('')}</div>
      <p style="font-size:0.8em;color:#888">Generated by Mi Marketing Factory</p>
    </body></html>`;
    fs.writeFileSync(htmlOut, html);
    return { success: true, output: { image_path: htmlOut, preview: htmlOut, template, status: 'html_fallback', note: 'Set COMFYUI_URL for image generation' }, duration_ms: Date.now() - t0, agent: 'marketing', metadata: { template } };
  } catch (e: any) {
    return { success: false, output: null, error: e.message, duration_ms: Date.now() - t0, agent: 'marketing', metadata: {} };
  }
}

export async function createVideo(script: string, style: string, duration = 30): Promise<AgentResult> {
  const t0 = Date.now();
  ensureDir(ARTIFACTS_DIR);
  const outPath = path.join(ARTIFACTS_DIR, `video_${Date.now()}.mp4`);

  // Route to Wan2.1 / Hunyuan via API
  const wanUrl = process.env.WAN_API_URL || process.env.HUNYUAN_URL;
  if (wanUrl) {
    return { success: true, output: { video_path: outPath, script: script.slice(0, 100), style, duration, status: 'queued_to_wan', api: wanUrl }, duration_ms: Date.now() - t0, agent: 'marketing', metadata: {} };
  }
  // Fallback: use ffmpeg to create a title card video
  return new Promise((resolve) => {
    exec(`ffmpeg -f lavfi -i color=c=black:size=1920x1080:duration=${duration} -vf "drawtext=text='${script.slice(0,50)}':fontsize=48:fontcolor=white:x=(w-text_w)/2:y=(h-text_h)/2" -c:v libx264 -t ${duration} "${outPath}" -y 2>&1`, { timeout: 60_000 }, (err, stdout) => {
      if (err) {
        resolve({ success: true, output: { video_path: null, status: 'ffmpeg_unavailable', note: 'Set WAN_API_URL for AI video generation, or install ffmpeg', script: script.slice(0,100) }, duration_ms: Date.now() - t0, agent: 'marketing', metadata: {} });
      } else {
        resolve({ success: true, output: { video_path: outPath, duration, style }, duration_ms: Date.now() - t0, agent: 'marketing', metadata: {} });
      }
    });
  });
}

export async function writeSeoArticle(topic: string, keywords: string[], length = 800): Promise<AgentResult> {
  const t0 = Date.now();
  ensureDir(ARTIFACTS_DIR);

  // Use local LLM if available (Ollama), else draft structure
  const ollamaUrl = process.env.OLLAMA_URL || 'http://localhost:11434';
  const prompt = `Write a ${length}-word SEO article about: ${topic}. Focus keywords: ${keywords.join(', ')}. Include H2 headers, meta description, internal linking opportunities. Tone: professional restaurant/food business.`;

  try {
    const article = await new Promise<string>((resolve, reject) => {
      const model = process.env.MI_DEFAULT_MODEL || 'llama3';
      const body = JSON.stringify({ model, prompt, stream: false });
      const req = require('http').request({ hostname: '127.0.0.1', port: 11434, path: '/api/generate', method: 'POST', headers: { 'Content-Type': 'application/json' }, timeout: 60_000 }, (res: any) => {
        let raw = ''; res.on('data', (d: any) => { raw += d; }); res.on('end', () => {
          try { resolve(JSON.parse(raw).response || ''); } catch { reject(new Error('Ollama parse failed')); }
        });
      }); req.on('error', reject); req.write(body); req.end();
    });
    const outPath = path.join(ARTIFACTS_DIR, `seo_${Date.now()}.md`);
    const full = `# ${topic}\n\n**Keywords:** ${keywords.join(', ')}\n\n${article}`;
    fs.writeFileSync(outPath, full);
    return { success: true, output: { article: full, word_count: article.split(/\s+/).length, file: outPath, keywords }, duration_ms: Date.now() - t0, agent: 'marketing', metadata: { topic } };
  } catch {
    // Structural draft without LLM
    const draft = [
      `# ${topic}`,
      `\n**Meta Description:** ${topic} — learn how Bakudan Ramen serves authentic Japanese ramen in San Antonio, TX.\n`,
      `## Introduction\n[Write ${Math.floor(length * 0.15)} words about ${topic}]\n`,
      `## Why ${keywords[0] || topic}?\n[${Math.floor(length * 0.2)} words]\n`,
      ...keywords.slice(1).map(kw => `## ${kw}\n[${Math.floor(length * 0.15)} words]\n`),
      `## Conclusion\n[${Math.floor(length * 0.1)} words]\n`,
      `\n*Generated draft — keywords: ${keywords.join(', ')}*`,
    ].join('\n');
    const outPath = path.join(ARTIFACTS_DIR, `seo_draft_${Date.now()}.md`);
    fs.writeFileSync(outPath, draft);
    return { success: true, output: { article: draft, word_count: length, file: outPath, keywords, note: 'Draft structure — set OLLAMA_URL for AI writing' }, duration_ms: Date.now() - t0, agent: 'marketing', metadata: { topic } };
  }
}

