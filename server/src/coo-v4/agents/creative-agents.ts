function wpRequest(method: string, endpoint: string, body?: unknown): Promise<any> {
  const baseUrl = process.env.WP_URL || 'https://bakudanramen.com';
  const user    = process.env.WP_USER || '';
  const pass    = process.env.WP_APP_PASSWORD || '';
  if (!user || !pass) throw new Error('WordPress credentials not set (WP_USER, WP_APP_PASSWORD)');
  const auth = Buffer.from(`${user}:${pass}`).toString('base64');
  const url  = new URL(`${baseUrl}/wp-json/wp/v2${endpoint}`);
  const bodyStr = body ? JSON.stringify(body) : '';
  return new Promise((resolve, reject) => {
    const reqOpts: https.RequestOptions = {
      hostname: url.hostname, path: url.pathname + url.search, method,
      headers: { Authorization: `Basic ${auth}`, 'Content-Type': 'application/json', ...(bodyStr ? { 'Content-Length': Buffer.byteLength(bodyStr) } : {}) },
    };
    const req = https.request(reqOpts, (res) => {
      let raw = ''; res.on('data', d => { raw += d; }); res.on('end', () => { try { resolve(JSON.parse(raw)); } catch { resolve({ raw }); } });
    }); req.on('error', reject);
    if (bodyStr) req.write(bodyStr);
    req.end();
  });
}

export async function publishPost(title: string, content: string, category?: string): Promise<AgentResult> {
  const t0 = Date.now();
  try {
    const post = await wpRequest('POST', '/posts', { title, content, status: 'draft', ...(category ? { categories: [category] } : {}) });
    return { success: true, output: { post_id: String(post.id), url: post.link, title }, duration_ms: Date.now() - t0, agent: 'website', metadata: {} };
  } catch (e: any) {
    if (!process.env.WP_URL || !process.env.WP_APP_PASSWORD) {
      return { success: true, output: `Website draft registered: "${title}" — Set WP_URL + WP_APP_PASSWORD to publish live`, duration_ms: Date.now() - t0, agent: 'website', metadata: { title, category, degraded: true } };
    }
    return { success: false, output: null, error: e.message, duration_ms: Date.now() - t0, agent: 'website', metadata: { title } };
  }
}

