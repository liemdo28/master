export async function schedulePosts(posts: Array<{ platform: string; content: string; scheduled_at: string }>): Promise<AgentResult> {
  const t0 = Date.now();
  ensureDir(ARTIFACTS_DIR);
  const schedFile = path.join(ARTIFACTS_DIR, 'social_schedule.json');
  const existing = fs.existsSync(schedFile) ? JSON.parse(fs.readFileSync(schedFile, 'utf8')) : [];
  const newPosts = posts.map(p => ({ ...p, id: `post_${Date.now()}_${Math.random().toString(36).slice(2,5)}`, status: 'scheduled', created_at: new Date().toISOString() }));
  fs.writeFileSync(schedFile, JSON.stringify([...existing, ...newPosts], null, 2));
  return { success: true, output: { scheduled: posts.length, failed: 0, schedule_file: schedFile }, duration_ms: Date.now() - t0, agent: 'social', metadata: {} };
}
