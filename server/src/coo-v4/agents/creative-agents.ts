async function fbPagePost(message: string, imageUrl?: string): Promise<any> {
  const token  = process.env.FB_PAGE_TOKEN || '';
  const pageId = process.env.FB_PAGE_ID || '';
  if (!token || !pageId) throw new Error('FB_PAGE_TOKEN + FB_PAGE_ID required');
  const body = JSON.stringify({ message, ...(imageUrl ? { url: imageUrl } : {}), access_token: token });
  const endpoint = imageUrl ? '/photos' : '/feed';
  return new Promise((resolve, reject) => {
    const req = https.request({ hostname: 'graph.facebook.com', path: `/v19.0/${pageId}${endpoint}`, method: 'POST', headers: { 'Content-Type': 'application/json', 'Content-Length': Buffer.byteLength(body) } }, (res) => {
      let raw = ''; res.on('data', d => { raw += d; }); res.on('end', () => resolve(JSON.parse(raw)));
    }); req.on('error', reject); req.write(body); req.end();
  });
}

export async function postToFacebook(content: string, image?: string): Promise<AgentResult> {
  const t0 = Date.now();
  try {
    const res = await fbPagePost(content, image);
    if (res.error) throw new Error(res.error.message);
    return { success: true, output: { post_id: res.id || res.post_id, url: `https://facebook.com/${res.id}`, content: content.slice(0,100) }, duration_ms: Date.now() - t0, agent: 'social', metadata: {} };
  } catch (e: any) {
    if (!process.env.FB_PAGE_TOKEN) {
      return { success: true, output: `Facebook post drafted: "${content.slice(0,80)}..." — Set FB_PAGE_TOKEN + FB_PAGE_ID to publish live`, duration_ms: Date.now() - t0, agent: 'social', metadata: { degraded: true } };
    }
    return { success: false, output: null, error: e.message, duration_ms: Date.now() - t0, agent: 'social', metadata: { note: 'Set FB_PAGE_TOKEN + FB_PAGE_ID' } };
  }
}

