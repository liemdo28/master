// ---------------------------------------------------------------------------
// Default data shape
// ---------------------------------------------------------------------------
function defaultData() {
  const providers = {}
  for (const [id, m] of Object.entries(META)) {
    providers[id] = { baseURL: m.baseURL, model: m.model, keys: [] }
  }
  // activeProviders: ordered array of provider ids that are "on"
  // mode: "fallback" (try next on error) | "roundrobin" (cycle each request)
  return { activeProviders: [], mode: 'fallback', providers }
}

// ---------------------------------------------------------------------------
// Persistence
// ---------------------------------------------------------------------------
function load() {
  if (!fs.existsSync(KEYS_FILE)) return defaultData()
  try {
    const d = JSON.parse(fs.readFileSync(KEYS_FILE, 'utf8'))
    if (!d.activeProviders) d.activeProviders = []
    if (!d.mode) d.mode = 'fallback'
    return d
  } catch { return defaultData() }
}

function save(data) {
  fs.writeFileSync(KEYS_FILE, JSON.stringify(data, null, 2), 'utf8')
  writeEnv(data)
}

function writeEnv(data) {
  const lines = ['# Auto-generated by API Key Manager', '']
  lines.push(`ACTIVE_PROVIDERS=${data.activeProviders.join(',')}`)
  lines.push(`PROVIDER_MODE=${data.mode}`)
  lines.push(`DEFAULT_PROVIDER=${data.activeProviders[0] || ''}`)
  lines.push('')
  for (const [id, p] of Object.entries(data.providers)) {
    const activeKey = p.keys?.find(k => k.active) || p.keys?.[0]
    lines.push(`# ${META[id]?.label || id}`)
    lines.push(`${id.toUpperCase()}_BASE_URL=${p.baseURL || ''}`)
    lines.push(`${id.toUpperCase()}_DEFAULT_MODEL=${p.model || ''}`)
    lines.push(`${id.toUpperCase()}_API_KEY=${activeKey?.value || ''}`)
    if ((p.keys?.length || 0) > 1)
      lines.push(`${id.toUpperCase()}_API_KEYS=${p.keys.map(k => k.value).join(',')}`)
    lines.push('')
  }
  fs.writeFileSync(ENV_OUT, lines.join('\n'), 'utf8')
}

